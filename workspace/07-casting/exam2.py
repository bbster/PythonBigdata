def exam2():
    '''
    bool(데이터) : 데이터가 있는지 없는지를 검사
                   => True: 데이터가 있음, False: 데이터가 없음
    '''
    # 숫자
    print(bool(100))
    print(bool(0))
    print('-'*30)
    
    # 문자열
    print(bool('abc'))
    print(bool(''))
    print('-'*30)
    
    # None
    print(bool(None))

    
exam2()